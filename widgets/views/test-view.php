        <h4>Fall/Winter/Spring/Summer Interns</h4>
        <p>The Maryland Women’s Business Center (MWBC) is seeking a bright, organized and energetic individual to help our organization improve economic opportunities for women in Montgomery County.  We will provide you with a great mission-oriented educational experience, in a fun and secure setting, working in close collaboration with experienced business development leaders in our region, exposing you to several possible directions for your career.</p>
        <h5>DUTIES AND RESPONSIBILITIES: <span>database management, marketing, surveying, and customer service</span></h5> 
        <h6>Event Planning and Management</h6>
        <ul>
            <li>Assist with management of monthly workshops including scheduling seminars, corresponding with speakers, room set-up, handling registration, attending the seminars, tabulating evaluations</li>
        </ul>
        <h6>Marketing/Writing</h6>
        <ul>
            <li>Help market MWBC’s workshops and services by writing and distributing a monthly flyers of events</li>
            <li>Updating MWBC’s website, Facebook page and Twitter</li>
            <li>Posting MWBC’s events on community calendars</li>
            <li>Identifying new outlets and creative forms of advertising to help spread the word</li>
        </ul>
        <h6>Business Development</h6>
        <ul>
            <li>Attend/observe one-on-one business counseling sessions between entrepreneurs and our business counselors</li>
            <li>Research and write case studies of success stories for publication</li>
        </ul>
        <h6>Research</h6>
        <ul>
            <li>Undertake research on various topics such as facts about women in business, lists of women business owners, examples of best practices in other organizations</li>
        </ul>
        <h6>Other Projects</h6>
        <ul>
            <li>Assist with mailing list/database management</li>
            <li>Answer phone, email or walk-in requests for information/referrals</li>
            <li>Represent and/or join MWBC staff in meetings with other groups</li>
            <li>Assist with development, collection and analysis of annual survey of outcomes</li>
        </ul>
        <h5>QUALIFICATIONS:</h5>
        <ul>
            <li>College or high school student with interest in entrepreneurship, business, economic development, and/or marketing.</li>
            <li>Experience with social media and WordPress is a plus but not required.</li>
            <li>Must be available a minimum of 10 hrs/week.</li>
        </ul>
        <h5>LOCATION:</h5>
        <p>Internship is located at the MWBC office at <a href="">51 Monroe Street, Rockville.</a> We are located in the Town Center of Rockville, across the street from the Rockville Metro Station on the Red Line.</p>
        <h5>CONTACT INFORMATION:</h5>
        <p>Send resume to: <a href="mailto:admin@rockvilleredi.org">admin@rockvilleredi.org</a></p>
