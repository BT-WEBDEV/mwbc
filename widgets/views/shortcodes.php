    <h1>ECONOMIC IMPACT</h1>
    <p>Women now create businesses at more than twice the rate of men.  Unfortunately, the path to starting and sustaining a successful enterprise that leads to a stable and substantial income for the owner is rocky and fraught with obstacles. As a result, too many women-owned businesses start without an adequate plan, fail to make the transition to CEO, or stay too small and end up operating below the margins of success or fail prematurely. We help women think strategically from the beginning about building a business that allows them to succeed over the long-term.</p>
    <p class="font-weight-bold my-4">We encourage women to take the steps that will lead to their success by helping them to figure out:</p>
    <div class="row">
        <div class="col-6">
            <ul>
                <li>How to hire their first employee;</li>
                <li>Obtain microloans and financing that will help them grow;</li>
                <li>Look for suitable and manageable office space beyond their homes;</li>
                <li>Build a network and connections to reduce their isolation; and</li>
                <li>Develop a business plan that will result in their growth, stability and independence.</li>
            </ul>
        </div>
        <div class="col-6">
            <p>Through training, individual counseling, facilitated peer group support, specialized resources, and access to capital, we help more than 1,500 women per year gain the skills, connections and confidence needed to successfully navigate the challenging path leading to sustainable business ownership as a career.</p>
        </div>
